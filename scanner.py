def check_trend(df):
 if len(df)<200:return False
 c=df["Close"]
 m50=c.rolling(50).mean();m150=c.rolling(150).mean();m200=c.rolling(200).mean()
 cur=float(c.iloc[-1]);a=float(m50.iloc[-1]);b=float(m150.iloc[-1])
 m20v=m200.dropna()
 if len(m20v)<1:return False
